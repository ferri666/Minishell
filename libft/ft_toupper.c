#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		c -= 32;
	if (c == -95)
		c = -127;
	if (c == -87)
		c = 119;
	if (c == -83)
		c = -115;
	if (c == -77)
		c = -109;
	if (c == -70)
		c = -102;
	return (c);
}
